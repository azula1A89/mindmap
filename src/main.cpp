// Simple helper function to load an image into a OpenGL texture with common settings
bool LoadTextureFromMemory(const void* data, size_t data_size, GLuint* out_texture, int* out_width, int* out_height)
{
    // Load from file
    int image_width = 0;
    int image_height = 0;
    unsigned char* image_data = stbi_load_from_memory((const unsigned char*)data, (int)data_size, &image_width, &image_height, NULL, 4);
    if (image_data == NULL)
        return false;

    // Create a OpenGL texture identifier
    GLuint image_texture;
    glGenTextures(1, &image_texture);
    glBindTexture(GL_TEXTURE_2D, image_texture);

    // Setup filtering parameters for display
    glTexParameteri(GL_TEXTURE_2D, GL_TEXTURE_MIN_FILTER, GL_LINEAR);
    glTexParameteri(GL_TEXTURE_2D, GL_TEXTURE_MAG_FILTER, GL_LINEAR);

    // Upload pixels into texture
    glPixelStorei(GL_UNPACK_ROW_LENGTH, 0);
    glTexImage2D(GL_TEXTURE_2D, 0, GL_RGBA, image_width, image_height, 0, GL_RGBA, GL_UNSIGNED_BYTE, image_data);
    auto encoded_str =  base64::to_base64(std::string_view((const char*)data, data_size));
    loaded_textures_base64[(ImTextureID)(intptr_t)image_texture] = std::make_shared<std::string>(encoded_str);
    stbi_image_free(image_data);

    *out_texture = image_texture;
    *out_width = image_width;
    *out_height = image_height;

    return true;
}

// Open and read a file, then forward to LoadTextureFromMemory()
bool LoadTextureFromFile(const char* file_name, GLuint* out_texture, int* out_width, int* out_height)
{
    FILE* f = fopen(file_name, "rb");
    if (f == NULL)
        return false;
    fseek(f, 0, SEEK_END);
    size_t file_size = (size_t)ftell(f);
    if (file_size == -1)
        return false;
    fseek(f, 0, SEEK_SET);
    void* file_data = IM_ALLOC(file_size);
    fread(file_data, 1, file_size, f);
    bool ret = LoadTextureFromMemory(file_data, file_size, out_texture, out_width, out_height);
    IM_FREE(file_data);
    return ret;
}

texture_t load_texture_from_file(std::string& filepath)
{
    texture_t ret = {0, ImVec2(0, 0)};
    if (!filepath.empty()){
        if (loaded_textures.find(filepath) != loaded_textures.end()) {
            ret = *loaded_textures[filepath];
        }else{
            GLuint texture(0);
            int width(0), height(0);
            if( LoadTextureFromFile(filepath.c_str(), &texture, &width, &height) ){
                ret = { (ImTextureID)(intptr_t)texture, ImVec2((float)width, (float)height) };
                loaded_textures[filepath] = std::make_shared<texture_t>(ret);
            }
        }
    }
    return ret;
}

texture_t load_texture_from_base64(std::string& base64)
{
    texture_t ret = {0, ImVec2(0, 0)};
    if (!base64.empty()){
        auto decoded = base64::from_base64(base64);
        GLuint texture(0);
        int width(0), height(0);
        if( LoadTextureFromMemory(decoded.data(), decoded.size(), &texture, &width, &height) ){
            ret = { (ImTextureID)(intptr_t)texture, ImVec2((float)width, (float)height) };
        }
    }
    return ret;
}

